Artisan::command('inspire', function () {
    $this->comment(Inspiring::quote());
})->describe('Display an inspiring quote');

Artisan::command('welcome:message_simple', function(){
	$this->info('console中欢迎执行此命令');
})->describe('打印欢迎信息');
